    private void tableDetailMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableDetailMouseClicked
        // TODO add your handling code here:
        int row = tableDetail.getSelectedRow();
        if (row != -1) {
            String id = tableDetail.getValueAt(row, 0).toString();
            String idBuku = tableDetail.getValueAt(row, 2).toString();
            String jumlah = tableDetail.getValueAt(row, 4).toString();

            if(parent != null){
                parent.setDataIDPengembalian(id, idBuku, jumlah);
            }else if(parents != null){
                parents.setDataIDPengembalian(id, idBuku, jumlah);
            }

            dispose();
        }
    }//GEN-LAST:event_tableDetailMouseClicked


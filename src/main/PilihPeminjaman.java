    private void loadData(String s) {
        DefaultTableModel model = (DefaultTableModel) tableDetail.getModel();
        model.setRowCount(0);

        String sql;
        PreparedStatement stmt;

        try {
             sql = "SELECT dp.id_peminjaman, dp.nama, dp.id_buku, dp.judul, dp.jumlah, " +
                    "CASE " +
                    "WHEN pg.id_pengembalian IS NOT NULL THEN 'Dikembalikan' " +
                    "WHEN p.tanggal_kembali < CURDATE() THEN 'Terlambat' " +
                    "ELSE 'Sedang Dipinjam' " +
                    "END AS status " +
                    "FROM detail_peminjaman dp " +
                    "JOIN peminjaman p ON dp.id_peminjaman = p.id_peminjaman " +
                    "LEFT JOIN pengembalian pg ON dp.id_peminjaman = pg.id_peminjaman AND dp.id_buku = pg.id_buku " +
                    "WHERE dp.id_peminjaman LIKE ?";
            stmt = connect.prepareStatement(sql);
            stmt.setString(1, "%" + s + "%");
            ResultSet rs = stmt.executeQuery();
            
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("id_peminjaman"),
                    rs.getString("nama"),
                    rs.getString("id_buku"),
                    rs.getString("judul"),
                    rs.getInt("jumlah"),
                    rs.getString("status")
                });
            }
            tableDetail.setModel(model);
            
        } catch (Exception e) {
            e.printStackTrace();
        }
}


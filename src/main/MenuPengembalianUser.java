    private String generateIDPengembalian() {
        try {
            String code = "PGB";
            String yyMM = new SimpleDateFormat("yyMM").format(new Date());
            String bookID = "";

            String sql = "SELECT COUNT(*) AS jumlah FROM pengembalian";
            Statement stmt = connect.createStatement();
            ResultSet rs = stmt.executeQuery(sql);

            int nomor = 1;
            if (rs.next()) {
                nomor = rs.getInt("jumlah") + 1;
            }
            bookID = code + yyMM + String.format("%04d", nomor);
            idKembali.setText(bookID);

            rs.close();
            stmt.close();
            return bookID;

        } catch (Exception e) {
            e.printStackTrace();
            return null;
        }
    }
    
    private void ProsesPengembalian(){
        if (adaPengembalian == null) {
            adaPengembalian = generateIDPengembalian();
        }
        String idPengembalian = adaPengembalian;
        String idPeminjaman = idPinjam.getText();
        String idBuku = IDBuku.getText();

    try {
        // Ambil tanggal jatuh tempo dari DB
        PreparedStatement getTanggal = connect.prepareStatement("SELECT tanggal_kembali FROM peminjaman WHERE id_peminjaman = ?");
        getTanggal.setString(1, idPeminjaman);
        ResultSet rs = getTanggal.executeQuery();

        if (rs.next()) {
            Date jatuhTempo = rs.getDate("tanggal_kembali");
            LocalDate jatuhTempoLocal = ((java.sql.Date) jatuhTempo).toLocalDate();

            // Ngambil tanggal kembali dari JDateChooser
            Date tglKembalii = tglKembali.getDate(); 
            LocalDate tglKembaliLocal = ((java.sql.Date) new java.sql.Date(tglKembalii.getTime())).toLocalDate();

            // Ngitung selisih hari jatoh tempo sama kembali
            long selisih = ChronoUnit.DAYS.between(jatuhTempoLocal, tglKembaliLocal);
            int denda = (int) Math.max(0, selisih) * 2000;
            
                // Insert pengembalian
                String sql = "INSERT INTO pengembalian (id_pengembalian, id_peminjaman, id_buku, tgl_kembali, denda) VALUES (?, ?, ?, ?, ?)";
                PreparedStatement insert = connect.prepareStatement(sql);
                insert.setString(1, idPengembalian);
                insert.setString(2, idPeminjaman);
                insert.setString(3, idBuku);
                if (tglKembali != null) {
                insert.setDate(4, new java.sql.Date(tglKembalii.getTime()));
                } else {
                    JOptionPane.showMessageDialog(this, "Tanggal kembali belum dipilih!");
                    return;
                }
                insert.setInt(5, denda);
                insert.executeUpdate();
      
                getData();
            
            int pilihan = JOptionPane.showConfirmDialog(this, "Mau kembalikan buku yang lain?", "Tambah Pengembalian Buku", JOptionPane.YES_NO_OPTION);
                if (pilihan == JOptionPane.NO_OPTION) {
                    adaPengembalian = null;
                    JOptionPane.showMessageDialog(this, "Pengembalian berhasil!\nKamu Dikenakan Denda: Rp" + denda);
                }

        }
        
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Gagal menyimpan pengembalian!");
    }
    }
    
   


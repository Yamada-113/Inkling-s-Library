    private void getData() {
        DefaultTableModel model = (DefaultTableModel) tableKembali.getModel();
        model.setRowCount(0);

        try {
            String sqlSelect = "SELECT id_pengembalian, id_peminjaman, id_buku, tgl_kembali, denda, " +
                                "CASE " +
                                "WHEN tgl_kembali >= CURDATE() THEN 'Dikembalikan' " +
                                "WHEN tgl_kembali < CURDATE() THEN 'Terlambat' " +
                                "END AS status " + "FROM pengembalian";
            PreparedStatement stmtSelect = connect.prepareStatement(sqlSelect);
            ResultSet rs = stmtSelect.executeQuery();

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("id_pengembalian"),
                    rs.getString("id_peminjaman"),
                    rs.getString("id_buku"),
                    rs.getDate("tgl_kembali"),
                    rs.getString("status"),
                    rs.getInt("denda")
                });
            }

            tableKembali.setModel(model);
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Gagal mengambil data!", "Error", JOptionPane.ERROR_MESSAGE);
        }
}

    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        if(updateJudul.getText().isEmpty() || updateKategori.getText().equals("Pilih Kategori") || 
            updateAuthor.getText().isEmpty() || updatePenerbit.getText().isEmpty() || updateStok.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Data Tidak Boleh Ada yang Kosong!", "Try Again", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
       
        try{
            String update = "UPDATE buku SET judul=?, kategori=?, author=?, penerbit=?, stok_buku=? WHERE id_buku=?";
            PreparedStatement setUpdate = connect.prepareStatement(update);
            setUpdate.setString(1, updateJudul.getText());
            setUpdate.setString(2, updateKategori.getText());
            setUpdate.setString(3, updateAuthor.getText());
            setUpdate.setString(4, updatePenerbit.getText());
            setUpdate.setString(5, updateStok.getText());
            setUpdate.setString(6, updateID.getText());
            setUpdate.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Data Berhasil Diupdate!", "Success", JOptionPane.INFORMATION_MESSAGE);
            jButton3.setText("Back");
            getData("");
            updateClearForm();
          
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Data Gagal Diupdate! XoX", "Try Again", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
    }//GEN-LAST:event_saveActionPerformed


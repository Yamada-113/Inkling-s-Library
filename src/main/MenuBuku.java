    private String generateIDBuku() {
    try {
        String code = "ILB";
        String yyMM = new SimpleDateFormat("yyMM").format(new Date());
        String bookID = "";

        String sql = "SELECT COUNT(*) AS jumlah FROM buku";
        Statement stmt = connect.createStatement();
        ResultSet rs = stmt.executeQuery(sql);

        int nomor = 1;
        if (rs.next()) {
            nomor = rs.getInt("jumlah") + 1;
        }
        bookID = code + yyMM + String.format("%04d", nomor);
        id.setText(bookID);

        rs.close();
        stmt.close();
        return bookID;
        
    } catch (Exception e) {
        e.printStackTrace();
        return null;
    }
}


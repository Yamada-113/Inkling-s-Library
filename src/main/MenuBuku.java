    private void getData(String s) {//ENKAPSULASI
      DefaultTableModel model = (DefaultTableModel) table.getModel();
       model.setRowCount(0);
        
        String sql;
        PreparedStatement stmt;
        
    try {
        if (s.isEmpty()) {
            sql = "SELECT * FROM buku";
            stmt = connect.prepareStatement(sql);
        } else {
            sql = "SELECT * FROM buku WHERE judul LIKE ?";
            stmt = connect.prepareStatement(sql);
            stmt.setString(1, "%" + s + "%");
        }
        ResultSet rs = stmt.executeQuery();
        
            while (rs.next()) {
                
            model.addRow(new Object[]{
                rs.getString("id_buku"),
                rs.getString("judul"),
                rs.getString("kategori"),
                rs.getString("author"),
                rs.getString("penerbit"),
                rs.getString("stok_buku"),
            });
            }

        table.setModel(model);
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data: " + e.getMessage(), 
                "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private void clicked(){
        int row = table.getSelectedRow();
        
        if(row >= 0){
            updateID.setText(table.getValueAt(row, 0).toString());
            updateJudul.setText(table.getValueAt(row, 1).toString());
            updateKategori.setText(table.getValueAt(row, 2).toString());
            updateAuthor.setText(table.getValueAt(row, 3).toString());
            updatePenerbit.setText(table.getValueAt(row, 4).toString());
            updateStok.setText(table.getValueAt(row, 5).toString());
        }
    }

